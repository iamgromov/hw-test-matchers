import sortByHealth from '../sortByHealth';

test('should sort heroes by health', () => {
  const heroList = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  expect(sortByHealth([...heroList])).toEqual(expected);
});
